package Practice1;

public class Else_If_Else {
public static void main(String[] args) {
int x=40;
if(x==8) {
	System.out.println("x value is 20");
}
else if(x==30) {
	System.out.println("x value is 30");
}
else if(x==50){
	System.out.println("x value is 40");
}
else {
	System.out.println("else statement executed");
}
}	

}
